    //load don xin nghi tu ngay bat dau
    void loadDonXinNghi() {
        DefaultTableModel model = (DefaultTableModel) jtbDonXinNghi.getModel();
        while (jtbDonXinNghi.getRowCount() > 0) {
            model.removeRow(0);

        }
        Date date = jdtpkTuNgay.getDate();
        ArrayList<dtoDonXinNghi> list = busDonXinNghi.getInstance().getListDonXinNgi(date);
        for (dtoDonXinNghi item : list) {
            model.addRow(new Object[]{item.getMADON(), item.getMANV(), item.getThoiGianLap(), item.getThoiGianBatDau(), item.getThoiGianKetThuc(), item.getLYDO()});
        }
    }

