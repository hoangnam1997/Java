    void loadbinDing() {
        jtbDonXinNghi.getSelectionModel().addListSelectionListener((e) -> {
            if (jtbDonXinNghi.getSelectedRow() >= 0) {
                Object madon = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 0);
                Object lydo = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 5);
                Object thoigianlap = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 2);
                Object thoigianbatdau = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 3);
                Object thoigianketthuc = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 4);
                Object manv = jtbDonXinNghi.getValueAt(jtbDonXinNghi.getSelectedRow(), 1);
                jtxtMa.setText(madon.toString());
                jtxtLyDo.setText(lydo.toString());
                jtxtThoiGianLap.setText(thoigianlap.toString());
                jtxtTuNgay.setText(thoigianbatdau.toString());
                jtxtDenNgay.setText(thoigianketthuc.toString());
                jtxtNhanVien.setText(manv.toString());
            }
        });
    }

