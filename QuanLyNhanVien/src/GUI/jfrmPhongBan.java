    void loadBinding() {
        jtbPhongBan.getSelectionModel().addListSelectionListener((e) -> {
            try {
                if (jtbPhongBan.getSelectedRow() >= 0) {
                    Object MaPhong = jtbPhongBan.getValueAt(jtbPhongBan.getSelectedRow(), 0);
                    Object TenPhong = jtbPhongBan.getValueAt(jtbPhongBan.getSelectedRow(), 1);
                    Object DiaChi = jtbPhongBan.getValueAt(jtbPhongBan.getSelectedRow(), 2);
                    jtxtMaPB.setText(MaPhong.toString());
                    jtxtTenPB.setText(TenPhong.toString());
                    jtxtDiaChi.setText(DiaChi.toString());
                }

            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        });
    }

