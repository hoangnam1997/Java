//Xem các phòng bang theo tên.
    private void jbtnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnXemActionPerformed

        ArrayList<dtoPhongBan> listView=new ArrayList<>();
        listView.clear();
        DefaultTableModel model = (DefaultTableModel) jtbPhongBan.getModel();
        while(jtbPhongBan.getRowCount()>0) {
            model.removeRow(0);
        }
        String txtXem = jtxtXem.getText();
        if (txtXem.length() <= 0) {
            listView = busPhongBan.getInstance().getDanhSachPhongBan();
        } else {
            listView = busPhongBan.getInstance().getDanhSachPhongBan(txtXem);
        }

        for (dtoPhongBan iem : listView) {
            model.addRow(new Object[]{iem.getMAPB(), iem.getTenPB(), iem.getDiaChi()});
        }

    }//GEN-LAST:event_jbtnXemActionPerformed

//Thực hiện thêm phòng ban
    private void jbtnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnThemActionPerformed
        // TODO add your handling code here:
        if(jtxtMaPB.getText().length()<=0)return;
        int MaPhongBan = Integer.parseInt(jtxtMaPB.getText());
        String Ten = jtxtTenPB.getText();
        String DiaChi = jtxtDiaChi.getText();
        if(!busPhongBan.getInstance().insertPhongBan(MaPhongBan, Ten, DiaChi))
        {
            JOptionPane.showConfirmDialog(null, "Vui lòng thực hiện lại!","Thông báo",-1,2);
            return;
        }
        else
        {
            JOptionPane.showConfirmDialog(null, "Thêm thành công!","Thông báo",-1,1);
        }
        jbtnXem.doClick();
    }//GEN-LAST:event_jbtnThemActionPerformed

    private void jtxtMaPBKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtMaPBKeyTyped
        char enter = evt.getKeyChar();
        if(!(Character.isDigit(enter))){
            evt.consume();
        }
        if(jtxtMaPB.getText().length()>=4)
        {
             evt.consume();
        }
    }//GEN-LAST:event_jtxtMaPBKeyTyped
