    void LoadThongTin(dtoTaiKhoan TaiKhoan) {
        dtoNhanVien NhanVien = busNhanVien.getInstance().getNhanVientheoTaiKhoan(TaiKhoan.getTAIKHOAN());
        if(NhanVien==null)
        {
            JOptionPane.showConfirmDialog(null, "Không tìm thấy thông tin nhân viên!","Thông báo",-1,2);
            this.dispose();
        }
        jtxtMaNhanVien.setText(String.valueOf(NhanVien.getMANV()));
        jtxtHoTen.setText(String.valueOf(NhanVien.getTENNV()));
        jtxtSDT.setText(String.valueOf(NhanVien.getSDT()));
        jtxtDiaChi.setText(String.valueOf(NhanVien.getDiaChi()));
        jtxtTaiKhoan.setText(String.valueOf(NhanVien.getTAIKHOAN()));
        jtxtMaHopDong.setText(String.valueOf(NhanVien.getMAHD()));
        if (NhanVien.getGioiTinh() == 0) {
            jrdNu.setSelected(true);
        } else {
            jrdNam.setSelected(true);
        }
    }

