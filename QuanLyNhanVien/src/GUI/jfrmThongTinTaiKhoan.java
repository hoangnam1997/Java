//Cập nhật thông tin nhân viêm
    private void jbtnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCapNhatActionPerformed
        int MANV = Integer.parseInt(jtxtMaNhanVien.getText());
        String TenNV = jtxtHoTen.getText();
        String SDT = jtxtSDT.getText();
        String DiaChi = jtxtDiaChi.getText();
        String TaiKhoan = jtxtTaiKhoan.getText();
        int MAHD = Integer.parseInt(jtxtMaHopDong.getText());
        int GioiTinh = jrdNam.isSelected() == true ? 1 : 0;
        if(!busNhanVien.getInstance().updateNhanVien(MANV, TenNV, GioiTinh, SDT, DiaChi, TaiKhoan, MAHD))
        {
            JOptionPane.showConfirmDialog(null, "Vui lòng thực hiện lại!","Thông báo",-1,2);
        }
        else{
            JOptionPane.showConfirmDialog(null, "Cập nhật thành công!","Thông báo",-1,1);
        }

    }//GEN-LAST:event_jbtnCapNhatActionPerformed
